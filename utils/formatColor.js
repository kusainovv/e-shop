export const formatColor = (color) => {
    switch(color) {
        case 'black':
            return 'черный'
        case 'red':
            return 'красный'
        case 'white':
            return 'белый'
        case 'blue':
            return 'синий'
        case 'hacki':
            return 'хаки'
    }
}